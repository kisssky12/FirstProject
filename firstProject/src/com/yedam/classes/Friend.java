package com.yedam.classes;

public class Friend {

	String name;
	String age;
	String Num;
	
	public Friend(String name, int age, String Num) {
		this.name = name;
		this.age = age;
		this.Num = Num;
	}
	
	public String name() {
		return name;
	}

	public String age() {
		return age;
	}

	public String Num() {
		return Num;
	}

}
